/* Prints a line of text onto an image */
void render(char *line, int colour, int top, int left, struct image *image) {
  for(int i = 0; i < 5;i++) {
    int c; size_t idx = 0;
    while( (c = line[idx++]) ) {
      for(int j = 4; j >= 0; j--) {
        if(1<<j & alpha[c-'@'].lines[i]) {
          image->data[(top+i)*image->width + 6*(idx-1) +5-j +left] = colour;
        }
      }
    }
  }
}
